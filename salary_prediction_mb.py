from sklearn.metrics import classification_report, accuracy_score, confusion_matrix

# Convert continuous Linear Regression predictions to the same categories for comparison
# We'll use the quantiles from the original 'Salary' column to define the bins

# Re-create the qcut bins using the entire 'Salary' column from the original df
# This ensures consistency in categories for both models.

# First, get the bin edges from the original df for salary categories
# This step is crucial to make sure the categorization is consistent.
# Since pd.qcut produces labels, we need to extract the bin edges or apply it consistently.

# A safer way to categorize the predictions is to use the actual 'Salary' column's quantiles directly.
# This ensures the boundaries for 'Low', 'Medium', 'High' are consistent with how y_clf was created.

def categorize_salary(salary_series, original_salary_for_bins):
    # Get the bin edges from the original salary column
    # qcut returns unique sorted bin edges if `retbins=True` is used and labels are custom.
    # However, if labels are custom, qcut automatically sets the bin edges. We can infer them.
    # Let's re-run qcut on the full salary to get consistent bin edges.

    # Create a temporary Series to get the bin edges that pandas qcut would use
    _, bins = pd.qcut(original_salary_for_bins, q=3, labels=['Low', 'Medium', 'High'], retbins=True)

    # Define explicit bin labels and use pd.cut with these bins
    labels = ['Low', 'Medium', 'High']
    return pd.cut(salary_series, bins=bins, labels=labels, include_lowest=True)

# Apply categorization to linear regression predictions using the original df's salary bins
y_pred_linear_categorized = categorize_salary(pd.Series(y_pred_linear, index=y_test.index), df['Salary'])

# Ensure the data types match for comparison
